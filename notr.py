def collatz_steps(n):
    # Инициализируем счетчик шагов.
    steps = 0

    # Пока n не станет равным 1, продолжаем выполнение алгоритма.
    while n != 1:
        # Если n четное, делим его на 2.
        if n % 2 == 0:
            n = n // 2
        # Если n нечетное, умножаем его на 3 и прибавляем 1.
        else:
            n = 3 * n + 1

        # Увеличиваем счетчик шагов на 1.
        steps += 1

    # Возвращаем общее количество шагов.
    return steps


# Задаем начальное значение n.
n = 123456789

# Вызываем функцию и печатаем количество шагов.
steps = collatz_steps(n)
print(f"Количество шагов для числа {n} до достижения 1: {steps}")
